const { Model, DataTypes } = require('sequelize');

const sequelize = require('../config/connection.js');
// Creates a Category model 
class Category extends Model {}
// Creates the columns our Category table will have
Category.init(
  {
    id: {
      type: DataTypes.INTEGER,
      allowNull: false,
      primaryKey: true,
      autoIncrement: true,
    },
    category_name: {
      type: DataTypes.TEXT,
      allowNull: false,
    },
  },
  {
    sequelize,
    timestamps: false,
    freezeTableName: true,
    underscored: true,
    modelName: 'category',
  }
);

module.exports = Category;
